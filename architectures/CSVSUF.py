def vectorize(inputs, choice):
    r""" Vectorize measurement y and tensor z
        (Column-major order)
    
    Args:
        inputs: y: [F H W] -> [5 256 286] if choice == 'y'
               z: [F C H W] -> [5 16 256 256] if choice == 'z'

    return:
        y_vec: [5*256*286,]
        z_vec: [5*16*256*256,]
    """
    if choice == 'y':
        y_t = inputs.transpose(1, 2).contiguous()
        y_vec = y_t.contiguous().view(-1)
        return y_vec
    elif choice == 'z':
        z_t = inputs.transpose(2, 3).contiguous()
        z_vec = z_t.contiguous().view(-1)
        return z_vec


def de_vectorize(inputs, shape):
    r""" return the matrix form of a vector
        (Column-major order)
    
    Args:
        inputs: [B F*C*H*W]
        
    return:
        outputs: [B F C H W]
    """
    B, F, C, H, W = shape
    out_mat = torch.reshape(inputs, (B, F, C, W, H))
    outputs = torch.transpose(out_mat, 3, 4)
    return outputs


## MLP Compensation
class MLP_cps(nn.Module):
    def __init__(self, in_chans, hidden_chans, out_chans, drop=0.3):
        super().__init__()
        self.mlp = nn.Sequential(
            nn.Conv3d(in_chans, hidden_chans, kernel_size=(1, 3, 3), padding=(0, 1, 1)),
            nn.ReLU(inplace=True),
            nn.Conv3d(hidden_chans, hidden_chans, kernel_size=(1, 3, 3), padding=(0, 1, 1)),
            nn.ReLU(inplace=True),
            nn.Conv3d(hidden_chans, out_chans, kernel_size=(1, 3, 3), padding=(0, 1, 1))
        )
        
        
    def forward(self, x):
        """
        Input: x [B F H W]
        Return: x [B F*C*H*W]
        """
        x = x.unsqueeze(2) # x [B F 1 H W]
        x = rearrange(x, 'B F C H W -> B C F H W')
        x = self.mlp(x)
        x = rearrange(x, 'B C F H W -> B (F C H W)')
        return x


## Noise Level Estimator
class NLE(nn.Module):
    def __init__(self, in_chans=16, hidden_chans=32, out_chans=16):
        super().__init__()
        self.pwconv = nn.Conv3d(in_chans, hidden_chans, kernel_size=1, bias=True)
        self.dwconv = nn.Conv3d(hidden_chans, hidden_chans, kernel_size=(1, 3, 3), padding=(0, 1, 1), groups=hidden_chans, bias=True)
        self.relu = nn.ReLU(inplace=True)
        self.avg_pool2d = nn.AdaptiveAvgPool2d(1)
        self.mlp = nn.Sequential(
            nn.Conv2d(hidden_chans, hidden_chans, kernel_size=1, padding=0, bias=True),
            nn.ReLU(inplace=True),
            nn.Conv2d(hidden_chans, hidden_chans, kernel_size=1, padding=0, bias=True),
            nn.ReLU(inplace=True),
            nn.Conv2d(hidden_chans, out_chans, kernel_size=1, padding=0, bias=True),
            nn.Softplus()
        )
        
    def forward(self, x):
        """
        Args:
            x: [B F C H W]
        Return:
            x: [B F C H W]
        """
        B, F, _, _, _ = x.shape
        x = rearrange(x, 'B F C H W -> B C F H W').contiguous()
        x = self.dwconv(self.pwconv(x))
        x = self.relu(x)
        x = rearrange(x, 'B C F H W -> (B F) C H W')
        x = self.mlp(self.avg_pool2d(x))                           # [(B F) C 1 1]
        x = rearrange(x, '(B F) C H W -> B F C H W', B=B, F=F)
        return x


class zNet(nn.Module):
    def __init__(self, in_chans=17, out_chans=16, channel=32):
        super().__init__()
        self.fusion = nn.Sequential(
            nn.Conv3d(in_chans, channel, kernel_size=(1, 3, 3), padding=(0, 1, 1)),
            nn.ReLU(inplace=True),
            nn.Conv3d(channel, channel, kernel_size=(1, 3, 3), padding=(0, 1, 1)),
            nn.ReLU(inplace=True),
            nn.Conv3d(channel, out_chans, kernel_size=(1, 3, 3), padding=(0, 1, 1))
        )
    
    def forward(self, x):
        """
        Inputs: [B 5 17 256 286]
        """
        x = rearrange(x, 'B F C H W -> B C F H W')
        x = self.fusion(x)
        x = rearrange(x, 'B C F H W -> B F C H W')
        return x[:, :, :, :256, :256]


## CSVSUF
class CSVSUF(nn.Module):
    ''' Main part of CSVSUF '''
    def __init__(self, opt):
        super().__init__()
        self.opt = opt
        self.num_iter = opt['num_iter']
        self.fution = zNet(in_chans=17, out_chans=16)
        
        # Gaussian Denoiser
        self.denoisers = nn.ModuleList([])
        for _ in range(opt['num_iter']):
            self.denoisers.append(
                STSPLT(
                    in_ch=opt['in_ch'],
                    out_ch=opt['out_ch'] if opt['MLP_Cps'] else 16,
                    embed_dim=opt['embed_dim'],
                    depths=opt['depths'],
                    num_heads=opt['num_heads'],
                    window_size=opt['window_size'],
                    NLE=opt['ANE'],
                    MLP_Cps=opt['MLP_Cps'],
                    qkv_bias=opt['qkv_bias'],
                    qk_scale=None,
                    drop_path_rate=opt['drop_path_rate'],
                    norm_layer=nn.LayerNorm,
                    use_checkpoint_attn=opt['use_checkpoint_attn'],
                    use_checkpoint_ffn=opt['use_checkpoint_ffn'],
                    no_checkpoint_attn_blocks=opt['no_checkpoint_attn_blocks'],
                    no_checkpoint_ffn_blocks=opt['no_checkpoint_ffn_blocks']
                )
            )
        
        # Compensation
        if opt['MLP_Cps']:
            self.mlp_cps = nn.ModuleList([])
            for _ in range(opt['num_iter'] - 1):
                self.mlp_cps.append(
                    MLP_cps(in_chans=1, hidden_chans=32, out_chans=16)
                )
        
        # Adaptive Noise Estimator
        if opt['ANE']:
            self.nle = nn.ModuleList([])
            for _ in range(opt['num_iter']):
                self.nle.append(
                    NLE(in_chans=16, hidden_chans=32, out_chans=16)
                )
                
    def z_init(self, y, masks_batch):
        [B, F, C, H, W] = masks_batch.shape
        masks_shift = torch.zeros(B, F, C, H, W + 2 * (C - 1)).cuda().float()
        for i in range(C):
            masks_shift[:, :, i, :, (i * 2):(i * 2 + W)] = masks_batch[:, :, i, :, :] # [B 5 16 256 286]
        y = y.unsqueeze(2)
        z = torch.cat([y, masks_shift], dim=2)
        z = self.fution(z)
        return z
    
    def forward(self, y, masks_batch, H_mtx, A_mtx):
        r"""
        Args:
            Inputs  y: [B F H W] -> [B 5 256 286]
                    masks_batch: [B F C H W]
                    H: [H*(W+step*(C-1))*F, F*C*H*W]
                    A: [F*C*H*W, H*(W+step*(C-1))*F]
            Output z: [B F C H W]
        """
        # Initialization
        B, F, C, H, W = masks_batch.shape
        # z = torch.zeros(B, F, C, H, W).cuda()
        z = self.z_init(y, masks_batch)
        x_temp = torch.zeros(B, F*C*H*W).cuda()
        c = torch.zeros(B, F*C*H*W).cuda()
        
        # Start iterating
        for i in range(self.num_iter):
            # X-update
            for j in range(B):
                temp_Hz = torch.mv(H_mtx, vectorize(z[j, :, :, :, :], choice='z'))
                temp_vec = vectorize(y[j, :, :, :], choice='y') - temp_Hz
                x_temp[j, :] = vectorize(z[j, :, :, :, :], choice='z') + torch.mv(A_mtx, temp_vec)
            
            if self.opt['MLP_Cps']:
                x = x_temp + c
            else:
                x = x_temp

            x = de_vectorize(x, shape=(B, F, C, H, W)) # x: [B F C H W]
            
            # Adaptive Noise Estimator
            if self.opt['ANE']:
                n = self.nle[i](x)              # n: [B F C 1 1]
                n = n.repeat(1, 1, 1, x.shape[3], x.shape[4])
            else:
                n = None
            
            ## Z-update
            if self.opt['MLP_Cps']:
                z, c = self.denoisers[i](x, n)
                if i < self.num_iter - 1:
                    c = self.mlp_cps[i](c)
            else:
                z = self.denoisers[i](x, n)

        return z

        